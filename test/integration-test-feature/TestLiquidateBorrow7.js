const liquidateBorrow = require('./methods/liquidate');

contract('liquidate-7: If we invoke `seize` function straightly, the tx should be failed', async (accounts) => {
    let ctx;
    before(async () => {
        let wbtc = await WBTC.deployed();
        let usdc = await USDC.deployed();
        let usdt = await USDT.deployed();
        let comptroller = await Comptroller.at(ComptrollerProxy.address)
        let dEther = await DEther.at(await comptroller.markets(0));
        let dWBTC = await DERC20.at(await comptroller.markets(1));
        let dUSDC = await DERC20.at(await comptroller.markets(2));
        let dUSDT = await DERC20.at(await comptroller.markets(3));
        let CFGT = await CycleGovToken.deployed();
        let CFSC = await CycleStableCoin.deployed();
        let borrowPool = await Borrows.at(BorrowsProxy.address);
        let exchangePool = await ExchangePool.deployed();
        let oracle = await TestOracle.deployed();
        // init context
        ctx = {
            comptroller: comptroller,
            borrowPool: borrowPool,
            exchangePool: exchangePool,
            CFGT: CFGT,
            CFSC: CFSC,
            dEther: dEther,
            dWBTC: dWBTC,
            dUSDT: dUSDT,
            dUSDC: dUSDC,
            WBTC: wbtc,
            USDC: usdc,
            USDT: usdt,
            oracle: oracle,
            IERC20: IERC20,
        };
    });
    it('If we invoke `seize` function straightly, the tx should be failed', async () => {
        let repayAmount = 100;
        let reason = "borrow pool mismatch";
        await liquidateBorrow.failSeize(ctx.dEther, accounts[1], accounts[0], repayAmount, reason);
        await liquidateBorrow.failSeize(ctx.dWBTC, accounts[1], accounts[0], repayAmount, reason);
        await liquidateBorrow.failSeize(ctx.dUSDC, accounts[1], accounts[0], repayAmount, reason);
        await liquidateBorrow.failSeize(ctx.dUSDT, accounts[1], accounts[0], repayAmount, reason);
    });
});
